def is_valid_email(email):
    return re.match(r'^[\w\.-]+@[\w\.-]+\.\w+$', email)

def load_users():
    users = {}
    if os.path.exists(USERS_FILE):
        with open(USERS_FILE, "r") as f:
            for line in f:
                email, password = line.strip().split("|")
                users[email] = password
    return users

def save_user(email, password):
    with open(USERS_FILE, "a") as f:
        f.write(f"{email}|{password}\n")

def signup():
    users = load_users()
    while True:
        email = input("Enter your email: ").strip()
        if not is_valid_email(email):
            print("‚ùå Invalid email format.")
            continue
        if email in users:
            print("‚ùå Email already registered.")
            return None
        break
    password = input("Enter your password: ")
    save_user(email, password)
    print("‚úÖ Signup successful.")
    return email

def login():
    users = load_users()
    email = input("Enter your email: ").strip()
    password = input("Enter your password: ")
    if users.get(email) == password:
        print("‚úÖ Login successful.")
        return email
    else:
        print("‚ùå Invalid credentials.")
        return None

def take_location():
    province = input("Enter your province: ")
    city = input("Enter your city: ")
    area = input("Enter your area: ")
    address = f"{area}, {city}, {province}"
    with open("addresses.txt", "a") as f:
        f.write(f"{address}\n")
    return address

def user_shop(email, inventory):
    address = take_location()
    name = input("Enter your name: ")
    phone = input("Enter phone number: ")
    customer = Customer(name, phone, address, email)
    categories = list(inventory.keys())

    while True:
        print("\nProduct Categories:")
        for i, c in enumerate(categories):
            print(f"{i+1}. {c}")
        choice = input("Choose category number or 'q' to quit: ")
        if choice.lower() == "q":
            break
        if choice.isdigit():
            idx = int(choice) - 1
            if 0 <= idx < len(categories):
                cat = categories[idx]
                while True:  # Stay inside this category until user presses 'b'
                    print(f"\n-- {cat} --")
                    for i, p in enumerate(inventory[cat]):
                        print(f"{i+1}. {p.name} - Rs. {p.price} (Qty: {p.quantity})")
                    p_choice = input("Select product number to buy, or 'b' to go back: ")
                    if p_choice.lower() == "b":
                        break
                    if p_choice.isdigit():
                        p_idx = int(p_choice) - 1
                        if 0 <= p_idx < len(inventory[cat]):
                            chosen = inventory[cat][p_idx]
                            if chosen.quantity <= 0:
                                print("‚ùå Sorry, this product is out of stock.")
                                continue
                            while True:
                                qty_str = input(f"Enter quantity to buy for '{chosen.name}' (Available: {chosen.quantity}): ")
                                if qty_str.isdigit():
                                    qty = int(qty_str)
                                    if 1 <= qty <= chosen.quantity:
                                        customer.add_to_cart(chosen, qty)
                                        chosen.quantity -= qty
                                        print(f"‚úÖ Added {qty} x {chosen.name} to cart.")
                                        break
                                    else:
                                        print(f"‚ùå Please enter a quantity between 1 and {chosen.quantity}")
                                else:
                                    print("‚ùå Enter a valid numeric quantity.")
                        else:
                            print("‚ùå Invalid product number.")
                    else:
                        print("‚ùå Invalid input, please enter a product number or 'b'.")

    if customer.cart:
        print("\n--- Order Summary ---")
        cart_summary = {}
        for item in customer.cart:
            cart_summary[item.name] = cart_summary.get(item.name, 0) + 1
